<p style="color: white; text-align: justify;">Grammar is a subject that stresses many students out, as it can be quite confusing and complicated. However, correct grammar is important for your writing and success, both as a student and as a future employee. It is thus crucial to know some simple methods to improve your grammar skills.</p>

<p style="color: white;">Here are seven basic tips to try!</p><br>

<h4 style="color: white;">1. Read</h4>
<h6 style="color: white;">Reading may be the number one way you can improve your grammar skills. When you read, you reinforce correct grammar in your mind. It can be particularly helpful to read out loud, as the combination of seeing, saying, and hearing assists in solidifying what you have learned. In addition to improving your grammar, reading will help with all aspects of your writing, from sentence fluency to increased vocabulary.</h6><hr style="height:2px;border-width:0;color:gray;background-color:yellow;">

<h4 style="color: white;">2. Get a grammar manual</h4>
<h6 style="color: white;">It is useful to have a thorough reference book nearby that you can consult when writing. This way, any time a grammatical question arises, you can quickly refer to the manual to obtain the answer. There are many high quality grammar and writing guidebooks on the market. Speak to a librarian or writing instructor for recommendations. </h6><hr style="height:2px;border-width:0;color:gray;background-color:yellow;">

<h4 style="color: white;">3. Review the basics</h4>
<h6 style="color: white;">While classes in English linguistics and writing may not be something you are interested in, it is vital to spend a bit of time learning or reviewing the basic principles. Do some research on the basic parts of speech, as well as on the common grammatical errors people tend to make.</h6><hr style="height:2px;border-width:0;color:gray;background-color:yellow;">

<h4 style="color: white;">4. Practice</h4>
<h6 style="color: white;">There are many excellent resources, both online and in print, conducive to improving your grammar skills. A quick Internet search will reveal a multitude of websites that offer grammar games and exercises. If you know that grammar is an area you struggle with, set aside a few minutes each day to complete grammar exercises.</h6><hr style="height:2px;border-width:0;color:gray;background-color:yellow;">

<h4 style="color: white;">5. Listen to others</h4>
<h6 style="color: white;">When instructors, writing lab staff, or writing tutors give you feedback, listen to them! Find out if you have a consistent issue with any topics in particular. For example, do you frequently receive feedback regarding run-on sentences or have issues with subject-verb agreement? If so, make sure that when you proofread your assignments, you pay special attention to those details. It may even be wise to create your own personalized checklist of items to be aware of in your writing. </h6><hr style="height:2px;border-width:0;color:gray;background-color:yellow;">

<h4 style="color: white;">6. Proofreadâ€¦out loud</h4>
<h6 style="color: white;">Sometimes when we re-read pieces we have written, our brain fills in the gaps of missing information. We do not always catch our own mistakes when we proofread. Reading what you have written out loud, preferably to someone else, is an efficient way to evaluate whether or not you have used correct grammar. You are more likely to notice your errors if you read the content aloud, rather than in your head to yourself. </h6><hr style="height:2px;border-width:0;color:gray;background-color:yellow;">

<h4 style="color: white;">7. Write</h4>
<h6 style="color: white;">Just as it helps to read a great deal, it will also benefit you to write more. The more you practice writing with proper grammar, the more naturally it will come to you. These are greats tips to help you with writing a college essay.</h6>
                   
