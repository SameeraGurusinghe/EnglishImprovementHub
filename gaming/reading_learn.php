                   <h4 style="color: white;">What is reading comprehension?</h4>

<p style="color: white;">Reading comprehension is simply the ability to understand what you read. Strong reading comprehension typically encompasses a variety of literacy skills needed to interpret and identify meanings within a text. Several elements like fluency, the ability to decode unfamiliar vocabulary and using context clues from the reading to identify key features of a text can all be components of effective reading comprehension.
</p>
